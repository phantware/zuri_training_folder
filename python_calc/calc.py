# Calculator designed in python code

numb1 = int(input('Enter your first number'))
numb2 = int(input('Enter your second number'))

# Addition

print('{} + {} ='.format(numb1, numb2), numb1 + numb2)

# Subtraction

print('{} + {} ='.format(numb1, numb2), numb1 - numb2)

# Division

print('{} + {} ='.format(numb1, numb2), numb1 / numb2)

# Multiplication

print('{} + {} ='.format(numb1, numb2), numb1 * numb2)